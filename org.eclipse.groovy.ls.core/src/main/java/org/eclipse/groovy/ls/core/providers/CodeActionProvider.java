    private CodeAction createDeleteUnusedDeclarationAction(String uri, String content,
            Diagnostic diag, boolean preferred) {
        try {
            Range declarationRange = findUnusedDeclarationRange(uri, content, diag);
            if (declarationRange == null) {
                return null;
            }

            TextEdit removeEdit = new TextEdit(declarationRange, "");

            CodeAction action = new CodeAction("Delete unused declaration");
            action.setKind(CodeActionKind.QuickFix);
            action.setDiagnostics(Collections.singletonList(diag));
            action.setIsPreferred(preferred);

            WorkspaceEdit edit = new WorkspaceEdit();
            edit.setChanges(Collections.singletonMap(uri, Collections.singletonList(removeEdit)));
            action.setEdit(edit);
            return action;
        } catch (Exception e) {
            GroovyLanguageServerPlugin.logError("Failed to create delete unused declaration action", e);
            return null;
        }
    }

    private Range findUnusedDeclarationRange(String uri, String content, Diagnostic diag)
            throws JavaModelException {
        if (diag == null || diag.getRange() == null) {
            return null;
        }

        org.eclipse.jdt.core.ICompilationUnit workingCopy = documentManager.getWorkingCopy(uri);
        if (workingCopy == null) {
            return null;
        }

        boolean methodDiagnostic = isUnusedMethodDiagnostic(diag);
        for (IType type : workingCopy.getTypes()) {
            Range match = findUnusedDeclarationRange(type, content, diag.getRange(), methodDiagnostic);
            if (match != null) {
                return match;
            }
        }
        return null;
    }

    private Range findUnusedDeclarationRange(IType type, String content, Range diagnosticRange,
            boolean methodDiagnostic)
            throws JavaModelException {
        if (methodDiagnostic) {
            for (IMethod method : type.getMethods()) {
                Range methodRange = matchUnusedDeclarationRange(method, content, diagnosticRange);
                if (methodRange != null) {
                    return methodRange;
                }
            }

            for (IType innerType : type.getTypes()) {
                Range nested = findUnusedDeclarationRange(innerType, content, diagnosticRange, true);
                if (nested != null) {
                    return nested;
                }
            }
            return null;
        }

        Range typeRange = matchUnusedDeclarationRange(type, content, diagnosticRange);
        if (typeRange != null) {
            return typeRange;
        }

        for (IType innerType : type.getTypes()) {
            Range nested = findUnusedDeclarationRange(innerType, content, diagnosticRange, false);
            if (nested != null) {
                return nested;
            }
        }

        return null;
    }

    private Range matchUnusedDeclarationRange(ISourceReference sourceRef, String content,
            Range diagnosticRange) throws JavaModelException {
        if (sourceRef == null) {
            return null;
        }

        ISourceRange nameRange = sourceRef.getNameRange();
        ISourceRange sourceRange = sourceRef.getSourceRange();
        if (nameRange == null || sourceRange == null
                || nameRange.getOffset() < 0 || sourceRange.getOffset() < 0) {
            return null;
        }

        PositionUtils.LineIndex lineIndex = PositionUtils.buildLineIndex(content);
        Range nameLspRange = new Range(
                lineIndex.offsetToPosition(nameRange.getOffset()),
                lineIndex.offsetToPosition(nameRange.getOffset() + nameRange.getLength()));
        if (!intersects(nameLspRange, diagnosticRange)) {
            return null;
        }

        int sourceStart = sourceRange.getOffset();
        int sourceEnd = sourceStart + sourceRange.getLength();
        return expandToWholeLines(content, sourceStart, sourceEnd);
    }

    private Range expandToWholeLines(String content, int startOffset, int endOffset) {
        int boundedStart = Math.max(0, Math.min(startOffset, content.length()));
        int boundedEnd = Math.max(boundedStart, Math.min(endOffset, content.length()));

        int lineStart = content.lastIndexOf('\n', Math.max(0, boundedStart - 1));
        lineStart = lineStart < 0 ? 0 : lineStart + 1;

        int lineEndSearchFrom = Math.max(lineStart, Math.max(0, boundedEnd - 1));
        int lineEnd = content.indexOf('\n', lineEndSearchFrom);
        lineEnd = lineEnd < 0 ? content.length() : lineEnd + 1;

        while (lineEnd < content.length()) {
            int nextLineEnd = content.indexOf('\n', lineEnd);
            int scanEnd = nextLineEnd < 0 ? content.length() : nextLineEnd;
            String nextLine = content.substring(lineEnd, scanEnd);
            if (!nextLine.trim().isEmpty()) {
                break;
            }
            lineEnd = nextLineEnd < 0 ? content.length() : nextLineEnd + 1;
        }

        PositionUtils.LineIndex lineIndex = PositionUtils.buildLineIndex(content);
        return new Range(lineIndex.offsetToPosition(lineStart), lineIndex.offsetToPosition(lineEnd));
    }

    private List<CodeAction> createAddImportActions(String uri, String content, Diagnostic diag) {
        List<CodeAction> actions = new ArrayList<>();

        try {
            // Extract the type name from the diagnostic message
            Object msgObj = diag.getMessage();
            String message = msgObj != null ? msgObj.toString() : null;
            String typeName = extractTypeNameFromMessage(message);
            if (typeName == null || typeName.isEmpty()) return actions;

            // Search the JDT classpath index for matching types
            List<String> candidates = searchClasspathForType(typeName, uri);

            // Also check imports already present to avoid duplicates
            Set<String> existingImports = getExistingImports(uri);

            for (String fqn : candidates) {
                if (existingImports.contains(fqn)) continue;

                // Return a lightweight code action with data — the actual
                // WorkspaceEdit is computed lazily in resolveCodeAction()
                // so that the code-action list appears instantly.
                CodeAction action = new CodeAction("Import '" + fqn + "'");
                action.setKind(CodeActionKind.QuickFix);
                action.setDiagnostics(Collections.singletonList(diag));

                com.google.gson.JsonObject data = new com.google.gson.JsonObject();
                data.addProperty("resolve", RESOLVE_KIND_ADD_IMPORT);
                data.addProperty("uri", uri);
                data.addProperty("fqn", fqn);
                action.setData(data);

                actions.add(action);
            }
        } catch (Exception e) {
            GroovyLanguageServerPlugin.logError("Failed to create add import actions", e);
        }

        return actions;
    }

    private List<CodeAction> createCreateTypeActions(String uri, String content, Diagnostic diag) {
        List<CodeAction> actions = new ArrayList<>();

        try {
            String unresolvedCandidate = extractUnresolvedTypeCandidate(getDiagnosticMessage(diag));
            if (unresolvedCandidate == null || unresolvedCandidate.isEmpty()) {
                return actions;
            }

            String simpleName = simpleTypeName(unresolvedCandidate);
            if (!isValidTypeIdentifier(simpleName)) {
                return actions;
            }

            String currentPackage = getCurrentPackageName(content);
            String targetPackage = unresolvedCandidate.contains(".")
                    ? unresolvedCandidate.substring(0, unresolvedCandidate.lastIndexOf('.'))
                    : currentPackage;
            Path targetPath = resolveNewTypePath(uri, currentPackage, targetPackage, simpleName);
            if (targetPath == null || Files.exists(targetPath)) {
                return actions;
            }

            String targetUri = targetPath.toUri().toString();
            actions.add(createCreateTypeAction("class", simpleName, targetPackage, targetUri, diag));
            actions.add(createCreateTypeAction("interface", simpleName, targetPackage, targetUri, diag));
            actions.add(createCreateTypeAction("trait", simpleName, targetPackage, targetUri, diag));
            actions.add(createCreateTypeAction("enum", simpleName, targetPackage, targetUri, diag));
        } catch (Exception e) {
            GroovyLanguageServerPlugin.logError("Failed to create unresolved type creation actions", e);
        }

        return actions;
    }

    private CodeAction createCreateTypeAction(String kind, String simpleName, String packageName,
            String targetUri, Diagnostic diag) {
        String source = buildNewTypeSource(kind, simpleName, packageName);
        CreateFile createFile = new CreateFile(targetUri);
        TextEdit initialContent = new TextEdit(
                new Range(new Position(0, 0), new Position(0, 0)),
                source);
        TextDocumentEdit docEdit = new TextDocumentEdit(
                new VersionedTextDocumentIdentifier(targetUri, null),
                List.of(Either.forLeft(initialContent)));

        WorkspaceEdit edit = new WorkspaceEdit();
        edit.setDocumentChanges(List.of(
                Either.forRight(createFile),
                Either.forLeft(docEdit)));

        CodeAction action = new CodeAction("Create " + kind + " '" + simpleName + "'");
        action.setKind(CodeActionKind.QuickFix);
        action.setDiagnostics(Collections.singletonList(diag));
        action.setEdit(edit);
        return action;
    }

    private String buildNewTypeSource(String kind, String simpleName, String packageName) {
        StringBuilder sb = new StringBuilder();
        if (packageName != null && !packageName.isBlank()) {
            sb.append(PACKAGE_PREFIX).append(packageName).append(LINE_SEPARATOR).append(LINE_SEPARATOR);
        }
        sb.append(kind).append(' ').append(simpleName).append(" {").append(LINE_SEPARATOR);
        sb.append("}").append(LINE_SEPARATOR);
        return sb.toString();
    }

    /**
     * Resolve a deferred code action by computing its WorkspaceEdit.
     * Called by the LSP {@code codeAction/resolve} handler.
     */
    public CodeAction resolveCodeAction(CodeAction codeAction) {
        Object rawData = codeAction.getData();
        if (rawData == null) return codeAction;

        com.google.gson.JsonObject data;
        if (rawData instanceof com.google.gson.JsonObject jo) {
            data = jo;
        } else {
            // LSP4J may deserialise data as a generic JsonElement
            try {
                data = com.google.gson.JsonParser.parseString(rawData.toString()).getAsJsonObject();
            } catch (Exception e) {
                return codeAction;
            }
        }

        String resolveKind = data.has("resolve") ? data.get("resolve").getAsString() : null;
        if (!RESOLVE_KIND_ADD_IMPORT.equals(resolveKind)) {
            return codeAction;
        }

        String uri = data.has("uri") ? data.get("uri").getAsString() : null;
        String fqn = data.has("fqn") ? data.get("fqn").getAsString() : null;
        if (uri == null || fqn == null) return codeAction;

        String content = documentManager.getContent(uri);
        if (content == null) return codeAction;

        int insertLine = findImportInsertLine(content);
        String importStatement = IMPORT_PREFIX + fqn + LINE_SEPARATOR;
        TextEdit insertEdit = new TextEdit(
                new Range(new Position(insertLine, 0), new Position(insertLine, 0)),
                importStatement);

        WorkspaceEdit edit = new WorkspaceEdit();
        edit.setChanges(Collections.singletonMap(uri, Collections.singletonList(insertEdit)));
        codeAction.setEdit(edit);

        return codeAction;
    }

    private CodeAction createAddAllMissingImportsAction(String uri, String content,
            List<Diagnostic> diagnostics, List<String> onlyKinds) {
        if (!wantsKind(onlyKinds, CodeActionKind.Source, SOURCE_KIND_ADD_MISSING_IMPORTS)) {
            return null;
        }

        try {
            Set<String> existingImports = getExistingImports(uri);
            MissingImportsResult missingImports =
                    collectMissingImports(uri, diagnostics, existingImports);
            Set<String> importsToAdd = missingImports.importsToAdd;
            List<Diagnostic> matchedDiagnostics = missingImports.matchedDiagnostics;

            if (importsToAdd.size() < 2) {
                return null;
            }

            int insertLine = findImportInsertLine(content);
            String importBlock = buildImportBlock(importsToAdd);

            TextEdit insertEdit = new TextEdit(
                    new Range(new Position(insertLine, 0), new Position(insertLine, 0)),
                    importBlock);

            CodeAction action = new CodeAction("Add all missing imports");
            action.setKind(SOURCE_KIND_ADD_MISSING_IMPORTS);
            action.setDiagnostics(matchedDiagnostics);

            WorkspaceEdit edit = new WorkspaceEdit();
            edit.setChanges(Collections.singletonMap(uri, Collections.singletonList(insertEdit)));
            action.setEdit(edit);

            return action;
        } catch (Exception e) {
            GroovyLanguageServerPlugin.logError("Failed to create add all missing imports action", e);
            return null;
        }
    }

    /**
     * Search the JDT classpath index for types matching the given simple name.
     * Returns a list of fully qualified type names found on the project's classpath.
     * Results are cached (LRU, 60 s TTL) to avoid duplicate searches within
     * the same code-action request cycle.
     */
    private List<String> searchClasspathForType(String simpleName, String uri) {
        // Check the cache first
        List<IJavaProject> javaProjects = getSearchProjectsForUri(uri);
        String projectKey = javaProjects.isEmpty()
                ? simpleName
                : simpleName + "#" + javaProjects.get(0).getElementName();
        CachedSearchResult cached = importSearchCache.get(projectKey);
        if (cached != null && !cached.isExpired()) {
            return new ArrayList<>(cached.results);
        }

        List<String> results = new ArrayList<>();

        try {
            if (javaProjects.isEmpty()) {
                GroovyLanguageServerPlugin.logInfo(
                        "[codeaction] No Java project found for import search");
                return results;
            }

            // Create search scope over all project classpaths
            IJavaSearchScope scope = SearchEngine.createJavaSearchScope(
                    javaProjects.toArray(new IJavaProject[0]), IJavaSearchScope.SOURCES
                            | IJavaSearchScope.APPLICATION_LIBRARIES
                            | IJavaSearchScope.SYSTEM_LIBRARIES
                            | IJavaSearchScope.REFERENCED_PROJECTS);

            // Collect matching type names
            List<TypeNameMatch> matches = new ArrayList<>();
            TypeNameMatchRequestor requestor = new TypeNameMatchRequestor() {
                @Override
                public void acceptTypeNameMatch(TypeNameMatch match) {
                    matches.add(match);
                }
            };

            // Use a progress monitor that checks the thread interrupt flag
            // so SearchEngine can be cancelled when the LSP request times out.
            IProgressMonitor searchMonitor = new IProgressMonitor() {
                private volatile boolean cancelled = false;
                @Override public void beginTask(String name, int totalWork) {}
                @Override public void done() {}
                @Override public void internalWorked(double work) {}
                @Override public boolean isCanceled() {
                    return cancelled || Thread.currentThread().isInterrupted();
                }
                @Override public void setCanceled(boolean value) { cancelled = value; }
                @Override public void setTaskName(String name) {}
                @Override public void subTask(String name) {}
                @Override public void worked(int work) {}
            };

                JdtSearchSupport.searchAllTypeNames(
                    null,                                    // any package
                    SearchPattern.R_EXACT_MATCH,
                    simpleName.toCharArray(),                // type simple name
                    SearchPattern.R_EXACT_MATCH | SearchPattern.R_CASE_SENSITIVE,
                    IJavaSearchConstants.TYPE,               // classes, interfaces, enums
                    scope,
                    requestor,
                    IJavaSearchConstants.FORCE_IMMEDIATE_SEARCH,
                    searchMonitor);

            // Convert matches to FQNs, filtering out internal/synthetic types
            for (TypeNameMatch match : matches) {
                String fqn = match.getFullyQualifiedName();
                if (isImportSearchCandidate(fqn)) {
                    results.add(fqn);
                    if (results.size() >= MAX_IMPORT_SUGGESTIONS) {
                        break;
                    }
                }
            }

            // Sort: prefer shorter package paths (more common types first)
            results.sort((a, b) -> {
                // Prefer types from common packages
                int scoreA = packagePriority(a);
                int scoreB = packagePriority(b);
                if (scoreA != scoreB) return Integer.compare(scoreA, scoreB);
                return a.compareTo(b);
            });

            GroovyLanguageServerPlugin.logInfo(
                    "[codeaction] Search for '" + simpleName + "' found " + results.size() + " candidates");

        } catch (Exception e) {
            GroovyLanguageServerPlugin.logError(
                    "[codeaction] JDT type search failed for '" + simpleName + "'", e);
        }

        importSearchCache.put(projectKey, new CachedSearchResult(results));
        return results;
    }

    private List<IJavaProject> getSearchProjectsForUri(String uri) {
        List<IJavaProject> scopedProjects = new ArrayList<>();
        Set<String> seenProjectNames = new HashSet<>();

        try {
            String normalizedUri = DocumentManager.normalizeUri(uri);
            URI fileUri = toFileLocationUri(normalizedUri);
            if (fileUri == null) {
                throw new IllegalArgumentException("Not a file URI: " + normalizedUri);
            }
            IFile[] files = ResourcesPlugin.getWorkspace().getRoot().findFilesForLocationURI(fileUri);

            for (IFile file : files) {
                IJavaProject javaProject = asSearchProject(file);
                if (javaProject != null && seenProjectNames.add(javaProject.getElementName())) {
                    scopedProjects.add(javaProject);
                }
            }
        } catch (Exception e) {
            GroovyLanguageServerPlugin.logInfo(
                    "[codeaction] Failed to scope import search by URI; falling back to all projects: "
                    + e.getMessage());
        }

        if (!scopedProjects.isEmpty()) {
            return scopedProjects;
        }

        // Fallback when the document cannot be mapped to a workspace project.
        List<IJavaProject> allProjects = new ArrayList<>();
        try {
            for (IProject project : ResourcesPlugin.getWorkspace().getRoot().getProjects()) {
                if (!project.isOpen()) {
                    continue;
                }
                IJavaProject javaProject = JavaCore.create(project);
                if (javaProject != null && javaProject.exists()) {
                    allProjects.add(javaProject);
                }
            }
        } catch (Exception e) {
            GroovyLanguageServerPlugin.logInfo(
                    "[codeaction] Failed to enumerate workspace projects for import search: "
                    + e.getMessage());
        }

        return allProjects;
    }

    /**
     * Assign a priority score to a package for sorting import suggestions.
     * Lower = better (shown first).
     */
    private int packagePriority(String fqn) {
        if (fqn.startsWith("java.lang.")) return 0;
        if (fqn.startsWith("java.util.")) return 1;
        if (fqn.startsWith("java.io.")) return 2;
        if (fqn.startsWith("java.nio.")) return 3;
        if (fqn.startsWith("java.net.")) return 4;
        if (fqn.startsWith("java.")) return 5;
        if (fqn.startsWith("groovy.")) return 6;
        if (fqn.startsWith("javax.") || fqn.startsWith("jakarta.")) return 7;
        if (fqn.startsWith("org.junit.")) return 8;
        if (fqn.startsWith("spock.")) return 8;
        return 10;
    }

    /**
     * Extract a type name from an error message like "unable to resolve class Foo".
     */
    private String extractTypeNameFromMessage(String message) {
        if (message == null) return null;

        // Common patterns from Groovy compiler
        // "unable to resolve class SomeType"
        // "Groovy:unable to resolve class SomeType"
        String marker = "unable to resolve class ";
        int idx = message.indexOf(marker);
        if (idx >= 0) {
            String rest = message.substring(idx + marker.length()).trim();
            // Take the first word (may be qualified)
            int end = 0;
            while (end < rest.length() &&
                    (Character.isJavaIdentifierPart(rest.charAt(end)) || rest.charAt(end) == '.')) {
                end++;
            }
            String fullName = rest.substring(0, end);
            // Return simple name
            int lastDot = fullName.lastIndexOf('.');
            return lastDot >= 0 ? fullName.substring(lastDot + 1) : fullName;
        }

        return null;
    }

    private String getDiagnosticMessage(Diagnostic diagnostic) {
        Object msgObj = diagnostic.getMessage();
        return msgObj != null ? msgObj.toString() : "";
    }

    private boolean isUnresolvedTypeDiagnostic(Diagnostic diagnostic) {
        String code = getDiagnosticCode(diagnostic);
        String message = getDiagnosticMessage(diagnostic);
        return (code != null && code.startsWith(DIAG_CODE_UNRESOLVED_TYPE))
                || message.contains("unable to resolve class")
                || message.contains("Groovy:unable to resolve class");
    }

