    private boolean isMissingInterfaceMemberDiagnostic(Diagnostic diagnostic) {
        String message = getDiagnosticMessage(diagnostic);
        if (message == null || message.isEmpty()) {
            return false;
        }

        return (message.contains("must be declared abstract or the method")
                && message.contains("must be implemented"))
                || message.contains("must implement the inherited abstract method");
    }

    private CodeAction createImplementMissingInterfaceMembersAction(String uri, String content,
            Diagnostic diag) {
        try {
            ModuleNode module = documentManager.getGroovyAST(uri);
            if (module == null) {
                return null;
            }

            int targetLine = Math.max(1, diag.getRange().getStart().getLine() + 1);
            ClassNode targetClass = findEnclosingClass(module, targetLine);
            if (targetClass == null) {
                String className = extractClassSimpleNameFromMessage(getDiagnosticMessage(diag));
                targetClass = findClassBySimpleName(module, className);
            }

            if (targetClass == null || targetClass.isInterface()) {
                return null;
            }

            List<MethodNode> missingMethods = findMissingInterfaceMethods(targetClass, module);
            int insertLine = findClassInsertLine(targetClass, content);
            String diagnosticMessage = getDiagnosticMessage(diag);
            String insertionText;
            if (missingMethods.isEmpty()) {
                if (hasConcreteMethodForDiagnosticSignature(targetClass, diagnosticMessage)) {
                    return null;
                }
                insertionText = buildMissingMethodStubFromDiagnostic(
                        targetClass, diagnosticMessage, content, insertLine);
            } else {
                insertionText = buildMissingMethodStubsText(targetClass, missingMethods, content, insertLine);
            }
            if (insertionText.isEmpty()) {
                return null;
            }

            TextEdit insertEdit = new TextEdit(
                    new Range(new Position(insertLine, 0), new Position(insertLine, 0)),
                    insertionText);

            CodeAction action = new CodeAction(
                    missingMethods.size() <= 1
                            ? "Implement inherited abstract member"
                            : "Implement inherited abstract members");
            action.setKind(CodeActionKind.QuickFix);
            action.setDiagnostics(Collections.singletonList(diag));
            action.setIsPreferred(true);

            WorkspaceEdit edit = new WorkspaceEdit();
            edit.setChanges(Collections.singletonMap(uri, Collections.singletonList(insertEdit)));
            action.setEdit(edit);

            return action;
        } catch (Exception e) {
            GroovyLanguageServerPlugin.logError("Failed to create implement missing members action", e);
            return null;
        }
    }

    private List<MethodNode> findMissingInterfaceMethods(ClassNode targetClass, ModuleNode currentModule) {
        List<MethodNode> requiredMethods = new ArrayList<>();
        Set<String> requiredSignatures = new TreeSet<>();
        Set<String> visitedInterfaces = new HashSet<>();

        ClassNode[] interfaces = targetClass.getInterfaces();
        if (interfaces == null || interfaces.length == 0) {
            return Collections.emptyList();
        }

        for (ClassNode ifaceRef : interfaces) {
            collectRequiredInterfaceMethodsRecursive(
                    ifaceRef,
                    currentModule,
                    requiredMethods,
                    requiredSignatures,
                    visitedInterfaces);
        }

        collectRequiredSuperclassMethodsRecursive(
                targetClass.getUnresolvedSuperClass(),
                currentModule,
                requiredMethods,
                requiredSignatures);

        List<MethodNode> missingMethods = new ArrayList<>();
        for (MethodNode required : requiredMethods) {
            if (!hasConcreteMethodInHierarchy(targetClass, required)) {
                missingMethods.add(required);
            }
        }

        return missingMethods;
    }

    private void collectRequiredInterfaceMethodsRecursive(ClassNode ifaceRef,
            ModuleNode currentModule,
            List<MethodNode> requiredMethods,
            Set<String> requiredSignatures,
            Set<String> visitedInterfaces) {
        if (ifaceRef == null) {
            return;
        }

        ClassNode resolved = TraitMemberResolver.resolveTraitClassNode(ifaceRef, currentModule, documentManager);
        String interfaceName = resolved.getName();
        if (interfaceName == null || !visitedInterfaces.add(interfaceName)) {
            return;
        }

        for (MethodNode method : resolved.getMethods()) {
            if (!isMethodRequiredForImplementation(method)) {
                continue;
            }
            String signature = methodSignatureKey(method);
            if (requiredSignatures.add(signature)) {
                requiredMethods.add(method);
            }
        }

        ClassNode[] superInterfaces = resolved.getInterfaces();
        if (superInterfaces != null) {
            for (ClassNode superInterface : superInterfaces) {
                collectRequiredInterfaceMethodsRecursive(
                        superInterface,
                        currentModule,
                        requiredMethods,
                        requiredSignatures,
                        visitedInterfaces);
            }
        }
    }

    private void collectRequiredSuperclassMethodsRecursive(ClassNode superClass,
            ModuleNode currentModule,
            List<MethodNode> requiredMethods,
            Set<String> requiredSignatures) {
        if (superClass == null) {
            return;
        }

        superClass = resolveLocalClassReference(superClass, currentModule);

        String superName = superClass.getName();
        if (superName == null
                || "java.lang.Object".equals(superName)
                || "groovy.lang.GroovyObject".equals(superName)) {
            return;
        }

        for (MethodNode method : superClass.getMethods()) {
            if (!isMethodRequiredForImplementation(method)) {
                continue;
            }
            String signature = methodSignatureKey(method);
            if (requiredSignatures.add(signature)) {
                requiredMethods.add(method);
            }
        }

        collectRequiredSuperclassMethodsRecursive(
                superClass.getUnresolvedSuperClass(),
                currentModule,
                requiredMethods,
                requiredSignatures);
    }

    private ClassNode resolveLocalClassReference(ClassNode candidate, ModuleNode currentModule) {
        if (candidate == null || currentModule == null) {
            return candidate;
        }

        String candidateName = candidate.getName();
        if (candidateName == null || candidateName.isEmpty()) {
            return candidate;
        }

        for (ClassNode classNode : currentModule.getClasses()) {
            if (candidateName.equals(classNode.getName())
                    || candidateName.equals(classNode.getNameWithoutPackage())
                    || simpleTypeName(candidateName).equals(classNode.getNameWithoutPackage())) {
                return classNode;
            }
        }
        return candidate;
    }

    private boolean isMethodRequiredForImplementation(MethodNode method) {
        if (method == null) {
            return false;
        }

        String name = method.getName();
        if (name == null || name.isEmpty() || name.startsWith("$")) {
            return false;
        }

        int modifiers = method.getModifiers();
        if (!Modifier.isAbstract(modifiers)
                || Modifier.isStatic(modifiers)
                || Modifier.isPrivate(modifiers)) {
            return false;
        }

        ClassNode declaringClass = method.getDeclaringClass();
        if (declaringClass != null) {
            String declaringName = declaringClass.getName();
            if ("java.lang.Object".equals(declaringName)
                    || "groovy.lang.GroovyObject".equals(declaringName)) {
                return false;
            }
        }

        return true;
    }

    private boolean hasConcreteMethodInHierarchy(ClassNode targetClass, MethodNode requiredMethod) {
        for (ClassNode current = targetClass; current != null; current = current.getSuperClass()) {
            List<MethodNode> candidates = current.getDeclaredMethods(requiredMethod.getName());
            for (MethodNode candidate : candidates) {
                if (hasSameParameterTypes(candidate, requiredMethod)
                        && !Modifier.isAbstract(candidate.getModifiers())) {
                    return true;
                }
            }
        }
        return false;
    }

    private boolean hasSameParameterTypes(MethodNode left, MethodNode right) {
        Parameter[] leftParams = left.getParameters();
        Parameter[] rightParams = right.getParameters();
        if (leftParams == null || rightParams == null) {
            return (leftParams == null || leftParams.length == 0)
                    && (rightParams == null || rightParams.length == 0);
        }
        if (leftParams.length != rightParams.length) {
            return false;
        }

        for (int i = 0; i < leftParams.length; i++) {
            String leftType = normalizeTypeName(leftParams[i].getType());
            String rightType = normalizeTypeName(rightParams[i].getType());
            if (!leftType.equals(rightType)) {
                return false;
            }
        }

        return true;
    }

    private String methodSignatureKey(MethodNode method) {
        StringBuilder sb = new StringBuilder();
        sb.append(method.getName()).append('(');
        Parameter[] parameters = method.getParameters();
        if (parameters != null) {
            for (int i = 0; i < parameters.length; i++) {
                if (i > 0) {
                    sb.append(',');
                }
                sb.append(normalizeTypeName(parameters[i].getType()));
            }
        }
        sb.append(')');
        return sb.toString();
    }

    private String normalizeTypeName(ClassNode type) {
        if (type == null) {
            return "def";
        }
        if (type.isArray()) {
            return normalizeTypeName(type.getComponentType()) + "[]";
        }
        String name = type.getName();
        if (name == null || name.isEmpty()) {
            name = type.getNameWithoutPackage();
        }
        return name == null || name.isEmpty() ? "def" : name;
    }

    private int findClassInsertLine(ClassNode targetClass, String content) {
        String[] lines = content.split("\\n", -1);
        if (lines.length == 0) {
            return 0;
        }

        int startLine = Math.max(0, targetClass.getLineNumber() - 1);
        int endLine = Math.max(startLine, targetClass.getLastLineNumber() - 1);
        endLine = Math.min(endLine, lines.length - 1);

        for (int line = endLine; line >= startLine; line--) {
            if (lines[line].trim().equals("}")) {
                return line;
            }
        }

        return endLine;
    }

    private String buildMissingMethodStubsText(ClassNode targetClass,
            List<MethodNode> missingMethods,
            String content,
            int insertLine) {
        String[] lines = content.split("\\n", -1);
        int classLine = Math.max(0, targetClass.getLineNumber() - 1);

        String classIndent = classLine < lines.length ? leadingWhitespace(lines[classLine]) : "";
        String indentUnit = inferIndentUnit(lines, classIndent, classLine, insertLine);
        String methodIndent = classIndent + indentUnit;
        String bodyIndent = methodIndent + indentUnit;

        StringBuilder sb = new StringBuilder();
        if (insertLine > 0 && insertLine - 1 < lines.length && !lines[insertLine - 1].trim().isEmpty()) {
            sb.append("\n");
        }

        for (int i = 0; i < missingMethods.size(); i++) {
            MethodNode method = missingMethods.get(i);

            sb.append(methodIndent).append("@Override\n");
            sb.append(methodIndent)
                    .append(renderType(method.getReturnType()))
                    .append(' ')
                    .append(method.getName())
                    .append('(')
                    .append(renderParameters(method.getParameters()))
                    .append(") {\n");
            sb.append(bodyIndent)
                    .append("throw new UnsupportedOperationException(\"Method '")
                    .append(method.getName())
                    .append("' is not implemented\")\n");
            sb.append(methodIndent).append("}\n");

            if (i + 1 < missingMethods.size()) {
                sb.append("\n");
            }
        }

        sb.append("\n");
        return sb.toString();
    }

    private String buildMissingMethodStubFromDiagnostic(ClassNode targetClass, String diagnosticMessage,
            String content, int insertLine) {
        String signature = extractMissingMethodSignature(diagnosticMessage);
        if (signature == null || signature.isEmpty()) {
            return "";
        }

        int parenIndex = signature.indexOf('(');
        int closeParenIndex = signature.lastIndexOf(')');
        if (parenIndex <= 0 || closeParenIndex < parenIndex) {
            return "";
        }

        String header = signature.substring(0, parenIndex).trim();
        int lastSpace = header.lastIndexOf(' ');
        String returnType = lastSpace > 0 ? simpleTypeName(header.substring(0, lastSpace).trim()) : "def";
        String methodName = lastSpace > 0 ? header.substring(lastSpace + 1).trim() : header;
        if (!isValidTypeIdentifier(methodName)) {
            return "";
        }

        String params = signature.substring(parenIndex + 1, closeParenIndex).trim();
        List<String> renderedParams = new ArrayList<>();
        if (!params.isEmpty()) {
            String[] pieces = params.split(",");
            for (int i = 0; i < pieces.length; i++) {
                String typeName = pieces[i].trim();
                renderedParams.add(simpleTypeName(typeName) + " arg" + (i + 1));
            }
        }

        String[] lines = content.split("\\n", -1);
        int classLine = Math.max(0, targetClass.getLineNumber() - 1);
        String classIndent = classLine < lines.length ? leadingWhitespace(lines[classLine]) : "";
        String indentUnit = inferIndentUnit(lines, classIndent, classLine, insertLine);
        String methodIndent = classIndent + indentUnit;
        String bodyIndent = methodIndent + indentUnit;

        StringBuilder sb = new StringBuilder();
        if (insertLine > 0 && insertLine - 1 < lines.length && !lines[insertLine - 1].trim().isEmpty()) {
            sb.append("\n");
        }
        sb.append(methodIndent).append("@Override\n");
        sb.append(methodIndent).append(returnType).append(' ').append(methodName).append('(')
                .append(String.join(", ", renderedParams)).append(") {\n");
        sb.append(bodyIndent)
                .append("throw new UnsupportedOperationException(\"Method '")
                .append(methodName)
                .append("' is not implemented\")\n");
        sb.append(methodIndent).append("}\n\n");
        return sb.toString();
    }

    private String extractMissingMethodSignature(String diagnosticMessage) {
        if (diagnosticMessage == null || diagnosticMessage.isEmpty()) {
            return null;
        }

        String marker = "the method '";
        int markerIndex = diagnosticMessage.indexOf(marker);
        if (markerIndex >= 0) {
            int start = markerIndex + marker.length();
            int end = diagnosticMessage.indexOf('\'', start);
            if (end > start) {
                return diagnosticMessage.substring(start, end).trim();
            }
        }

        String inheritedMarker = "must implement the inherited abstract method ";
        int inheritedIndex = diagnosticMessage.indexOf(inheritedMarker);
        if (inheritedIndex >= 0) {
            return diagnosticMessage.substring(inheritedIndex + inheritedMarker.length()).trim();
        }

        return null;
    }

    private boolean hasConcreteMethodForDiagnosticSignature(ClassNode targetClass, String diagnosticMessage) {
        String signature = extractMissingMethodSignature(diagnosticMessage);
        if (signature == null || signature.isEmpty()) {
            return false;
        }

        int parenIndex = signature.indexOf('(');
        int closeParenIndex = signature.lastIndexOf(')');
        if (parenIndex <= 0 || closeParenIndex < parenIndex) {
            return false;
        }

        String header = signature.substring(0, parenIndex).trim();
        int lastSpace = header.lastIndexOf(' ');
        String methodName = lastSpace > 0 ? header.substring(lastSpace + 1).trim() : header;
        String params = signature.substring(parenIndex + 1, closeParenIndex).trim();
        String[] expectedTypes = params.isEmpty() ? new String[0] : params.split(",");

        for (ClassNode current = targetClass; current != null; current = current.getSuperClass()) {
            for (MethodNode candidate : current.getDeclaredMethods(methodName)) {
                if (Modifier.isAbstract(candidate.getModifiers())) {
                    continue;
                }
                Parameter[] candidateParams = candidate.getParameters();
                int candidateLength = candidateParams == null ? 0 : candidateParams.length;
                if (candidateLength != expectedTypes.length) {
                    continue;
                }
                boolean allMatch = true;
                for (int i = 0; i < expectedTypes.length; i++) {
                    String expectedType = expectedTypes[i].trim();
                    String actualType = normalizeTypeName(candidateParams[i].getType());
                    if (!actualType.equals(expectedType)
                            && !simpleTypeName(actualType).equals(simpleTypeName(expectedType))) {
                        allMatch = false;
                        break;
                    }
                }
                if (allMatch) {
                    return true;
                }
            }
        }
        return false;
    }

    private String renderType(ClassNode type) {
        if (type == null) {
            return "def";
        }
        if (type.isArray()) {
            return renderType(type.getComponentType()) + "[]";
        }
        String simpleName = type.getNameWithoutPackage();
        if (simpleName == null || simpleName.isEmpty()) {
            simpleName = type.getName();
        }
        return simpleName == null || simpleName.isEmpty() ? "def" : simpleName;
    }

    private String renderParameters(Parameter[] parameters) {
        if (parameters == null || parameters.length == 0) {
            return "";
        }

        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < parameters.length; i++) {
            if (i > 0) {
                sb.append(", ");
            }
            Parameter parameter = parameters[i];
            String name = sanitizeParameterName(parameter.getName(), i + 1);
            sb.append(renderType(parameter.getType())).append(' ').append(name);
        }
        return sb.toString();
    }

    private String sanitizeParameterName(String rawName, int position) {
        if (rawName == null || rawName.isEmpty()) {
            return "arg" + position;
        }
        if (!Character.isJavaIdentifierStart(rawName.charAt(0))) {
            return "arg" + position;
        }
        for (int i = 1; i < rawName.length(); i++) {
            if (!Character.isJavaIdentifierPart(rawName.charAt(i))) {
                return "arg" + position;
            }
        }
        return rawName;
    }

    private String inferIndentUnit(String[] lines, String classIndent, int classLine, int insertLine) {
        int start = Math.max(0, classLine + 1);
        int end = Math.min(lines.length - 1, Math.max(classLine, insertLine));

        for (int i = start; i <= end; i++) {
            String line = lines[i];
            if (line.trim().isEmpty()) {
                continue;
            }

            String indent = leadingWhitespace(line);
            if (indent.length() > classIndent.length() && indent.startsWith(classIndent)) {
                String delta = indent.substring(classIndent.length());
                if (!delta.isEmpty()) {
                    return delta;
                }
            }
        }

        return "    ";
    }

    private String leadingWhitespace(String line) {
        int i = 0;
        while (i < line.length()) {
            char c = line.charAt(i);
            if (c != ' ' && c != '\t') {
                break;
            }
            i++;
        }
        return line.substring(0, i);
    }

    private ClassNode findEnclosingClass(ModuleNode module, int targetLine) {
        ClassNode best = null;
        for (ClassNode classNode : module.getClasses()) {
            int start = classNode.getLineNumber();
            int end = classNode.getLastLineNumber();
            boolean enclosesTarget = start > 0 && end >= start
                    && targetLine >= start && targetLine <= end;
            boolean declaredBeforeTarget = start > 0 && start <= targetLine;
            if ((enclosesTarget || declaredBeforeTarget)
                    && (best == null || start >= best.getLineNumber())) {
                best = classNode;
            }
        }
        return best;
    }

    private ClassNode findClassBySimpleName(ModuleNode module, String simpleName) {
        if (simpleName == null || simpleName.isEmpty()) {
            return null;
        }
        for (ClassNode classNode : module.getClasses()) {
            if (simpleName.equals(classNode.getNameWithoutPackage())) {
                return classNode;
            }
        }
        return null;
    }

    private String extractClassSimpleNameFromMessage(String message) {
        if (message == null || message.isEmpty()) {
            return null;
        }

        String quotedCandidate = extractQuotedTypeCandidate(message);
        if (quotedCandidate != null) {
            return simpleTypeName(quotedCandidate);
        }

        String markerCandidate = extractTypeCandidateAfterMarker(message, TYPE_MESSAGE_MARKER);
        if (markerCandidate != null) {
            return simpleTypeName(markerCandidate);
        }

        return null;
    }

    /**
     * Get the set of fully qualified names already imported in a document.
     */
    private Set<String> getExistingImports(String uri) {
        Set<String> imports = new TreeSet<>();
        ModuleNode ast = documentManager.getGroovyAST(uri);
        if (ast != null) {
            for (ImportNode imp : ast.getImports()) {
                if (imp.getType() != null) {
                    imports.add(imp.getType().getName());
                }
            }
        }
        return imports;
    }

    /**
     * Find the line number where new imports should be inserted.
     * Returns the line after the last import statement, or after the package
     * declaration, or line 0 if neither exists.
     */
    private int findImportInsertLine(String content) {
        String[] lines = content.split("\n", -1);
        int lastImportLine = -1;
        int packageLine = -1;

        for (int i = 0; i < lines.length; i++) {
            String trimmed = lines[i].trim();
            if (trimmed.startsWith("package ")) {
                packageLine = i;
            }
            if (trimmed.startsWith(IMPORT_PREFIX)) {
                lastImportLine = i;
            }
            // Stop scanning after first class/interface/enum/trait/def
            if (trimmed.startsWith("class ") || trimmed.startsWith("interface ")
                    || trimmed.startsWith("enum ") || trimmed.startsWith("trait ")
                    || trimmed.startsWith("@") || trimmed.startsWith("def ")
                    || trimmed.startsWith("public ") || trimmed.startsWith("abstract ")
                    || trimmed.startsWith("final ")) {
                break;
            }
        }

        if (lastImportLine >= 0) {
            return lastImportLine + 1;
        }
        if (packageLine >= 0) {
            return packageLine + 2; // blank line after package
        }
        return 0;
    }

