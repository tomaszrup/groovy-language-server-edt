    private CodeAction createAddAllMissingImportsQuickFixAction(String uri, String content,
            List<Diagnostic> diagnostics) {
        try {
            MissingImportsResult missingImports =
                    collectMissingImports(uri, diagnostics, getExistingImports(uri));

            if (missingImports.importsToAdd.size() < 2) {
                return null;
            }

            TextEdit insertEdit = createAddImportsEdit(content, missingImports.importsToAdd);

            CodeAction action = new CodeAction("Add all missing imports");
            action.setKind(CodeActionKind.QuickFix);
            action.setDiagnostics(missingImports.matchedDiagnostics);

            WorkspaceEdit edit = new WorkspaceEdit();
            edit.setChanges(Collections.singletonMap(uri, Collections.singletonList(insertEdit)));
            action.setEdit(edit);

            return action;
        } catch (Exception e) {
            GroovyLanguageServerPlugin.logError("Failed to create add all missing imports quick fix action", e);
            return null;
        }
    }

    private CodeAction createFixAllAction(String uri, String content,
            List<Diagnostic> diagnostics, List<String> onlyKinds) {
        if (!wantsKind(onlyKinds, CodeActionKind.Source, CodeActionKind.SourceFixAll, SOURCE_KIND_FIX_ALL_GROOVY)) {
            return null;
        }

        List<TextEdit> edits = new ArrayList<>();
        TextEdit organizeImportsEdit = createOrganizeImportsEdit(uri, content, diagnostics);
        if (organizeImportsEdit != null) {
            edits.add(organizeImportsEdit);
        }
        edits.addAll(createDeleteUnusedDeclarationEdits(uri, content, diagnostics));
        if (edits.isEmpty()) {
            return null;
        }

        CodeAction action = new CodeAction("Fix all auto-fixable issues");
        action.setKind(getFixAllActionKind(onlyKinds));
        action.setDiagnostics(diagnostics);

        WorkspaceEdit edit = new WorkspaceEdit();
        edit.setChanges(Collections.singletonMap(uri, edits));
        action.setEdit(edit);
        return action;
    }

    private String getFixAllActionKind(List<String> onlyKinds) {
        if (onlyKinds != null
                && onlyKinds.contains(SOURCE_KIND_FIX_ALL_GROOVY)
                && !onlyKinds.contains(CodeActionKind.SourceFixAll)) {
            return SOURCE_KIND_FIX_ALL_GROOVY;
        }
        return CodeActionKind.SourceFixAll;
    }

    private List<TextEdit> createRemoveUnusedImportEdits(String uri, String content) {
        ModuleNode ast = documentManager.getGroovyAST(uri);
        if (ast == null) return Collections.emptyList();

        Set<String> unusedImportLines = UnusedImportDetector.findUnusedImportLines(ast, content);
        if (unusedImportLines.isEmpty()) return Collections.emptyList();

        List<TextEdit> edits = new ArrayList<>();
        String[] lines = content.split("\n", -1);

        for (int i = lines.length - 1; i >= 0; i--) {
            String trimmed = lines[i].trim();
            if (unusedImportLines.contains(trimmed)) {
                edits.add(new TextEdit(
                        new Range(new Position(i, 0), new Position(i + 1, 0)),
                        ""));
            }
        }

        return edits;
    }

    private TextEdit createOrganizeImportsEdit(String uri, String content, List<Diagnostic> diagnostics) {
        OrganizedImportsResult organized = organizeImports(uri, content, diagnostics);
        if (organized == null || !organized.changed) {
            return null;
        }
        return new TextEdit(organized.range, organized.replacementText);
    }

    private OrganizedImportsResult organizeImports(String uri, String content, List<Diagnostic> diagnostics) {
        String[] lines = content.split("\n", -1);
        int firstImportLine = -1;
        int lastImportLine = -1;
        List<String> preservedImportBlockLines = new ArrayList<>();
        Set<String> regularImports = new TreeSet<>((left, right) -> {
            int score = Integer.compare(packagePriority(left), packagePriority(right));
            return score != 0 ? score : left.compareTo(right);
        });
        int regularImportInsertIndex = -1;

        Set<String> unusedImportLines = Collections.emptySet();
        ModuleNode ast = documentManager.getGroovyAST(uri);
        if (ast != null) {
            unusedImportLines = UnusedImportDetector.findUnusedImportLines(ast, content);
        }

        for (int i = 0; i < lines.length; i++) {
            String trimmed = lines[i].trim();
            if (!trimmed.startsWith(IMPORT_PREFIX)) {
                continue;
            }

            if (firstImportLine < 0) {
                firstImportLine = i;
            }
            lastImportLine = i;

            if (unusedImportLines.contains(trimmed)) {
                continue;
            }

            Matcher simpleImportMatcher = SIMPLE_IMPORT_PATTERN.matcher(trimmed);
            if (trimmed.startsWith("import static ") || !simpleImportMatcher.matches()) {
                preservedImportBlockLines.add(lines[i]);
                continue;
            }

            String importedName = simpleImportMatcher.group(1);
            if (importedName.endsWith(".*") || trimmed.contains(" as ")) {
                preservedImportBlockLines.add(lines[i]);
                continue;
            }

            if (regularImportInsertIndex < 0) {
                regularImportInsertIndex = preservedImportBlockLines.size();
            }
            regularImports.add(importedName);
        }

        MissingImportsResult missingImports =
                collectMissingImports(uri, diagnostics, new HashSet<>(regularImports));
        regularImports.addAll(missingImports.importsToAdd);

        int endLineExclusive = lastImportLine >= 0 ? lastImportLine + 1 : 0;
        while (endLineExclusive < lines.length && lines[endLineExclusive].trim().isEmpty()) {
            endLineExclusive++;
        }

        if (firstImportLine >= 0) {
            preserveInterleavedImportComments(lines, firstImportLine, endLineExclusive, preservedImportBlockLines);
        }

        String replacement = buildOrganizedImportBlock(
                regularImports,
                preservedImportBlockLines,
                regularImportInsertIndex,
                firstImportLine >= 0 || !regularImports.isEmpty() || !preservedImportBlockLines.isEmpty());
        if (firstImportLine < 0) {
            if (replacement.isEmpty()) {
                return null;
            }
            int insertLine = findImportInsertLine(content);
            Range insertRange = new Range(new Position(insertLine, 0), new Position(insertLine, 0));
            return new OrganizedImportsResult(insertRange, replacement, true);
        }

        Range replaceRange = new Range(new Position(firstImportLine, 0), new Position(endLineExclusive, 0));
        String existingBlock = sliceLines(lines, firstImportLine, endLineExclusive);
        boolean changed = !existingBlock.equals(replacement);
        return new OrganizedImportsResult(replaceRange, replacement, changed);
    }

    private void preserveInterleavedImportComments(String[] lines, int startLine, int endLineExclusive,
            List<String> preservedImportBlockLines) {
        for (int i = startLine; i < endLineExclusive; i++) {
            String trimmed = lines[i].trim();
            if (!trimmed.isEmpty() && !trimmed.startsWith(IMPORT_PREFIX)) {
                preservedImportBlockLines.add(lines[i]);
            }
        }
    }

    private String buildOrganizedImportBlock(Set<String> regularImports,
            List<String> preservedImportBlockLines, int regularImportInsertIndex,
            boolean includeTrailingBlankLine) {
        List<String> organizedLines = new ArrayList<>(preservedImportBlockLines);
        List<String> regularImportLines = new ArrayList<>();
        for (String importName : regularImports) {
            regularImportLines.add(IMPORT_PREFIX + importName);
        }
        int insertIndex = regularImportInsertIndex >= 0 ? regularImportInsertIndex : 0;
        organizedLines.addAll(insertIndex, regularImportLines);

        while (!organizedLines.isEmpty() && organizedLines.get(organizedLines.size() - 1).trim().isEmpty()) {
            organizedLines.remove(organizedLines.size() - 1);
        }

        StringBuilder sb = new StringBuilder();
        for (String line : organizedLines) {
            sb.append(line).append(LINE_SEPARATOR);
        }
        if (!organizedLines.isEmpty() && includeTrailingBlankLine) {
            sb.append(LINE_SEPARATOR);
        }
        return sb.toString();
    }

    private TextEdit createAddImportsEdit(String content, Set<String> importsToAdd) {
        int insertLine = findImportInsertLine(content);
        String importBlock = buildImportBlock(importsToAdd);
        return new TextEdit(
                new Range(new Position(insertLine, 0), new Position(insertLine, 0)),
                importBlock);
    }

    private List<TextEdit> createDeleteUnusedDeclarationEdits(String uri, String content,
            List<Diagnostic> diagnostics) {
        List<TextEdit> edits = new ArrayList<>();
        if (diagnostics == null || diagnostics.isEmpty()) {
            return edits;
        }

        for (Diagnostic diagnostic : diagnostics) {
            if (!DIAG_CODE_UNUSED_DECLARATION.equals(getDiagnosticCode(diagnostic))) {
                continue;
            }
            Range range = findUnusedDeclarationRangeSafely(uri, content, diagnostic);
            if (range != null) {
                edits.add(new TextEdit(range, ""));
            }
        }

        edits.sort((left, right) -> compareRanges(left.getRange(), right.getRange()));
        List<TextEdit> filtered = new ArrayList<>();
        for (TextEdit edit : edits) {
            if (filtered.isEmpty()) {
                filtered.add(edit);
                continue;
            }
            Range previousRange = filtered.get(filtered.size() - 1).getRange();
            if (intersects(previousRange, edit.getRange())) {
                continue;
            }
            filtered.add(edit);
        }
        return filtered;
    }

    private IJavaProject asSearchProject(IFile file) {
        IProject project = file.getProject();
        if (project == null || !project.isOpen()) {
            return null;
        }

        IJavaProject javaProject = JavaCore.create(project);
        if (javaProject == null || !javaProject.exists()) {
            return null;
        }
        return javaProject;
    }

    private boolean isImportSearchCandidate(String fqn) {
        if (fqn == null || fqn.isEmpty()) {
            return false;
        }
        if (fqn.contains("$")) {
            return false;
        }
        return !fqn.startsWith("sun.")
                && !fqn.startsWith("com.sun.")
                && !fqn.startsWith("jdk.internal.");
    }

    private MissingImportsResult collectMissingImports(String uri,
            List<Diagnostic> diagnostics,
            Set<String> existingImports) {
        Set<String> importsToAdd = new TreeSet<>();
        List<Diagnostic> matchedDiagnostics = new ArrayList<>();

        for (Diagnostic diag : diagnostics) {
            String candidate = resolveMissingImportCandidate(uri, diag, existingImports, importsToAdd);
            if (candidate != null) {
                importsToAdd.add(candidate);
                matchedDiagnostics.add(diag);
            }
        }

        return new MissingImportsResult(importsToAdd, matchedDiagnostics);
    }

    private String resolveMissingImportCandidate(String uri,
            Diagnostic diag,
            Set<String> existingImports,
            Set<String> importsToAdd) {
        if (!isUnresolvedTypeDiagnostic(diag)) {
            return null;
        }

        String typeName = extractTypeNameFromMessage(getDiagnosticMessage(diag));
        if (typeName == null || typeName.isEmpty()) {
            return null;
        }

        List<String> candidates = searchClasspathForType(typeName, uri);
        for (String candidate : candidates) {
            if (!existingImports.contains(candidate) && !importsToAdd.contains(candidate)) {
                return candidate;
            }
        }
        return null;
    }

    private String buildImportBlock(Set<String> importsToAdd) {
        StringBuilder importBlock = new StringBuilder();
        for (String fqn : importsToAdd) {
            importBlock.append(IMPORT_PREFIX).append(fqn).append(LINE_SEPARATOR);
        }
        return importBlock.toString();
    }

    private Range findUnusedDeclarationRangeSafely(String uri, String content, Diagnostic diag) {
        try {
            return findUnusedDeclarationRange(uri, content, diag);
        } catch (Exception e) {
            GroovyLanguageServerPlugin.logError("Failed to resolve unused declaration range", e);
            return null;
        }
    }

    private boolean isUnusedMethodDiagnostic(Diagnostic diag) {
        String message = getDiagnosticMessage(diag);
        return message.contains("method '") && message.contains(" is never used");
    }

    private int compareRanges(Range left, Range right) {
        int startCompare = comparePositions(left.getStart(), right.getStart());
        if (startCompare != 0) {
            return startCompare;
        }
        return comparePositions(right.getEnd(), left.getEnd());
    }

    private int comparePositions(Position left, Position right) {
        int lineCompare = Integer.compare(left.getLine(), right.getLine());
        if (lineCompare != 0) {
            return lineCompare;
        }
        return Integer.compare(left.getCharacter(), right.getCharacter());
    }

    private boolean intersects(Range left, Range right) {
        if (left == null || right == null) {
            return false;
        }
        if (comparePositions(left.getStart(), left.getEnd()) == 0) {
            return containsPosition(right, left.getStart());
        }
        if (comparePositions(right.getStart(), right.getEnd()) == 0) {
            return containsPosition(left, right.getStart());
        }
        return comparePositions(left.getStart(), right.getEnd()) < 0
                && comparePositions(right.getStart(), left.getEnd()) < 0;
    }

    private boolean containsPosition(Range range, Position position) {
        return comparePositions(range.getStart(), position) <= 0
                && comparePositions(position, range.getEnd()) <= 0;
    }

    private String sliceLines(String[] lines, int startInclusive, int endExclusive) {
        StringBuilder sb = new StringBuilder();
        for (int i = startInclusive; i < endExclusive; i++) {
            sb.append(lines[i]);
            if (i + 1 < endExclusive || endExclusive < lines.length) {
                sb.append(LINE_SEPARATOR);
            }
        }
        return sb.toString();
    }

    private String extractUnresolvedTypeCandidate(String message) {
        if (message == null || message.isEmpty()) {
            return null;
        }
        String marker = "unable to resolve class ";
        int idx = message.indexOf(marker);
        if (idx < 0) {
            return null;
        }

        String rest = message.substring(idx + marker.length()).trim();
        int end = 0;
        while (end < rest.length()
                && (Character.isJavaIdentifierPart(rest.charAt(end)) || rest.charAt(end) == '.')) {
            end++;
        }
        return end == 0 ? null : rest.substring(0, end);
    }

    private String getCurrentPackageName(String content) {
        if (content == null || content.isEmpty()) {
            return "";
        }
        Matcher matcher = PACKAGE_DECLARATION_PATTERN.matcher(content);
        if (!matcher.find()) {
            return "";
        }
        String pkg = matcher.group(1);
        return pkg == null ? "" : pkg.trim();
    }

    private boolean isValidTypeIdentifier(String candidate) {
        if (candidate == null || candidate.isEmpty()) {
            return false;
        }
        if (!Character.isJavaIdentifierStart(candidate.charAt(0))) {
            return false;
        }
        for (int i = 1; i < candidate.length(); i++) {
            if (!Character.isJavaIdentifierPart(candidate.charAt(i))) {
                return false;
            }
        }
        return true;
    }

    private Path resolveNewTypePath(String currentUri, String currentPackage, String targetPackage,
            String simpleName) {
        Path currentPath = toFilePath(DocumentManager.normalizeUri(currentUri));
        if (currentPath == null) {
            return null;
        }
        try {
            Path currentParent = currentPath.getParent();
            if (currentParent == null) {
                return null;
            }

            Path sourceRoot = resolveSourceRoot(currentParent, currentPackage);
            if (sourceRoot == null) {
                sourceRoot = currentParent;
            }

            Path targetDir = sourceRoot;
            if (targetPackage != null && !targetPackage.isBlank()) {
                for (String segment : targetPackage.split("\\.")) {
                    if (!segment.isEmpty()) {
                        targetDir = targetDir.resolve(segment);
                    }
                }
            }
            return targetDir.resolve(simpleName + ".groovy");
        } catch (Exception e) {
            GroovyLanguageServerPlugin.logInfo(
                    "[codeaction] Failed to resolve new type path for " + currentUri + ": " + e.getMessage());
            return null;
        }
    }

    private URI toFileLocationUri(String uri) {
        if (uri == null || uri.isBlank()) {
            return null;
        }
        try {
            URI parsed = URI.create(uri);
            return "file".equals(parsed.getScheme()) ? parsed : null;
        } catch (Exception e) {
            return null;
        }
    }

    private Path toFilePath(String uri) {
        URI fileUri = toFileLocationUri(uri);
        if (fileUri == null) {
            return null;
        }
        try {
            return Paths.get(fileUri);
        } catch (Exception e) {
            return null;
        }
    }

    private Path resolveSourceRoot(Path currentParent, String currentPackage) {
        String normalizedParent = currentParent.toString().replace('\\', '/');
        for (String rootSegment : SOURCE_ROOT_SEGMENTS) {
            int idx = normalizedParent.indexOf(rootSegment);
            if (idx >= 0) {
                return Paths.get(normalizedParent.substring(0, idx + rootSegment.length() - 1));
            }
            String withoutTrailingSlash = rootSegment.substring(0, rootSegment.length() - 1);
            if (normalizedParent.endsWith(withoutTrailingSlash)) {
                return currentParent;
            }
        }

        if (currentPackage == null || currentPackage.isBlank()) {
            return currentParent;
        }

        String[] segments = currentPackage.split("\\.");
        Path sourceRoot = currentParent;
        for (int i = 0; i < segments.length && sourceRoot != null; i++) {
            sourceRoot = sourceRoot.getParent();
        }
        return sourceRoot;
    }

    private String extractQuotedTypeCandidate(String message) {
        int firstQuote = message.indexOf('\'');
        if (firstQuote < 0) {
            return null;
        }

        int secondQuote = message.indexOf('\'', firstQuote + 1);
        if (secondQuote <= firstQuote + 1) {
            return null;
        }

        return message.substring(firstQuote + 1, secondQuote).trim();
    }

    private String extractTypeCandidateAfterMarker(String message, String marker) {
        int markerIndex = message.indexOf(marker);
        if (markerIndex < 0) {
            return null;
        }

        String rest = message.substring(markerIndex + marker.length()).trim();
        int end = 0;
        while (end < rest.length()
                && (Character.isJavaIdentifierPart(rest.charAt(end)) || rest.charAt(end) == '.')) {
            end++;
        }

        if (end == 0) {
            return null;
        }
        return rest.substring(0, end);
    }

    private String simpleTypeName(String fqnOrSimple) {
        int dot = fqnOrSimple.lastIndexOf('.');
        return dot >= 0 ? fqnOrSimple.substring(dot + 1) : fqnOrSimple;
    }

    private static final class CodeActionRequest {
        private final String uri;
        private final String content;
        private final Range range;
        private final List<String> onlyKinds;
        private final List<Diagnostic> requestDiagnostics;
        private final List<Diagnostic> allDiagnostics;
        private final boolean wantQuickfix;
        private final boolean wantSource;

        private CodeActionRequest(String uri,
                String content,
                Range range,
                List<String> onlyKinds,
                List<Diagnostic> requestDiagnostics,
                List<Diagnostic> allDiagnostics,
                boolean wantQuickfix,
                boolean wantSource) {
            this.uri = uri;
            this.content = content;
            this.range = range;
            this.onlyKinds = onlyKinds;
            this.requestDiagnostics = requestDiagnostics;
            this.allDiagnostics = allDiagnostics;
            this.wantQuickfix = wantQuickfix;
            this.wantSource = wantSource;
        }
    }

    private static final class MissingImportsResult {
        private final Set<String> importsToAdd;
        private final List<Diagnostic> matchedDiagnostics;

        private MissingImportsResult(Set<String> importsToAdd,
                List<Diagnostic> matchedDiagnostics) {
            this.importsToAdd = importsToAdd;
            this.matchedDiagnostics = matchedDiagnostics;
        }
    }

    private static final class OrganizedImportsResult {
        private final Range range;
        private final String replacementText;
        private final boolean changed;

        private OrganizedImportsResult(Range range, String replacementText, boolean changed) {
            this.range = range;
            this.replacementText = replacementText;
            this.changed = changed;
        }
    }

    private boolean wantsKind(List<String> onlyKinds, String... expectedKinds) {
        if (onlyKinds == null) {
            return true;
        }

        for (String expectedKind : expectedKinds) {
            if (onlyKinds.contains(expectedKind)) {
                return true;
            }
        }

        return false;
    }
